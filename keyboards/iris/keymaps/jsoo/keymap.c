void td_braces_open(qk_tap_dance_state_t *state, void *user_data) {
  switch (state->count) {
  case 1:
    tap_code(KC_LPRN);
    break;
  case 2:
    tap_code(KC_LBRC);
    break;
  default:
    tap_code(KC_LCBR);
    break;
  }
}

void td_braces_close(qk_tap_dance_state_t *state, void *user_data) {
  switch (state->count) {
  case 1:
    tap_code(KC_RPRN);
    break;
  case 2:
    tap_code(KC_RBRC);
    break;
  default:
    tap_code(KC_RCBR);
    break;
  }
}

qk_tap_dance_action_t tap_dance_actions[] = {
    [TD_BRACES_OPEN] = ACTION_TAP_DANCE_FN(td_braces_open),
    [TD_BRACES_CLOSE] = ACTION_TAP_DANCE_FN(td_braces_close)
};

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
  }
  return true;
}
